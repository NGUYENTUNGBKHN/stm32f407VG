
    .syntax unified
    .arch armv7e-m

    /* Vector table */
    .section .isr_vector,"ax"
    .align 4
    .globl _isr_vector
_isr_vector:
    .word _estack
    .word Reset_Handler
    .word NMI_Handler
    .word HardFault_Handler
    .word MemoryManagement_Handler
    .word BusFault_Handler
    .word UsageFault_Handler
    .word 0
    .word 0
    .word 0
    .word 0
    .word SVC_Handler
    .word DebugMon_Handler
    .word 0
    .word PendSV_Handler
    .word SysTick_Handler

    .size _isr_vector, .-_isr_vector

/* Reset handler  */
    .text
    .thumb
    .thumb_func
    .align 1
    .globl Reset_Handler
.type Reset_Handler, %function
Reset_Handler:
    ldr r1, =_sidata
    ldr r2, =_sdata
    ldr r3, =_edata

    subs r3, r3, r2
    ble .L_loop1_done

.L_loop1:
    subs r3, r3, #4
    ldr r0, [r1,r3]
    str r0, [r2,r3]
    bgt .L_loop1

.L_loop1_done:

    ldr r1, =_sbss
    ldr r2, =_ebss

    movs r0, 0

    subs r2, r2, r1
    ble .L_loop3_done

.L_loop3:
    subs r2, r2, #4
    str r0, [r1, r2]
    bgt .L_loop3

.L_loop3_done:


    bl main

    .pool
    .size   Reset_Handler,.-Reset_Handler

.section ".text"


